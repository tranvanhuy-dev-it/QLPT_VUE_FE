import { useAuthStore } from '../stores/auth.js';
import { useRouter } from 'vue-router';
import { ref, computed } from 'vue';

export default {
  name: 'Sidebar',
  setup() {
    const authStore = useAuthStore();
    const router = useRouter();

    const username = computed(() => authStore.username);
    const role = computed(() => authStore.role);
    const isSidebarOpen = computed(() => authStore.isSidebarOpen);
    const closeSidebar = () => authStore.closeSidebar();

    const userInitial = computed(() => {
      const name = username.value || 'User';
      return name.charAt(0).toUpperCase();
    });

    const roleLabel = computed(() => {
      const r = role.value;
      if (r === 'LANDLORD') return 'Chủ trọ';
      if (r === 'TENANT') return 'Khách thuê';
      if (r === 'ADMIN') return 'Quản trị';
      return 'Thành viên';
    });

    const landlordGeneral = [
      {
        to: '/landlord',
        label: 'Tổng quan',
        icon: `<svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke="currentColor"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M4 6a2 2 0 012-2h2a2 2 0 012 2v4a2 2 0 01-2 2H6a2 2 0 01-2-2V6zM14 6a2 2 0 012-2h2a2 2 0 012 2v4a2 2 0 01-2 2h-2a2 2 0 01-2-2V6zM4 16a2 2 0 012-2h2a2 2 0 012 2v4a2 2 0 01-2 2H6a2 2 0 01-2-2v-4zM14 16a2 2 0 012-2h2a2 2 0 012 2v4a2 2 0 01-2 2h-2a2 2 0 01-2-2v-4z" /></svg>`
      }
    ];

    const landlordManage = [
      {
        to: '/landlord/boarding-houses',
        label: 'Dãy nhà trọ',
        icon: `<svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke="currentColor"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M19 21V5a2 2 0 00-2-2H7a2 2 0 00-2 2v16m14 0h2m-2 0h-5m-9 0H3m2 0h5M9 7h1m-1 4h1m4-4h1m-1 4h1m-5 10v-5a1 1 0 011-1h2a1 1 0 011 1v5m-4 0h4" /></svg>`
      },
      {
        to: '/landlord/rooms',
        label: 'Phòng trọ',
        icon: `<svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke="currentColor"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M3 12l2-2m0 0l7-7 7 7M5 10v10a1 1 0 001 1h3m10-11l2 2m-2-2v10a1 1 0 01-1 1h-3m-6 0a1 1 0 001-1v-4a1 1 0 011-1h2a1 1 0 011 1v4a1 1 0 001 1m-6 0h6" /></svg>`
      },
      {
        to: '/landlord/tenants',
        label: 'Khách thuê',
        icon: `<svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke="currentColor"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M17 20h5v-2a3 3 0 00-5.356-1.857M17 20H7m10 0v-2c0-.656-.126-1.283-.356-1.857M7 20H2v-2a3 3 0 015.356-1.857M7 20v-2c0-.656.126-1.283.356-1.857m0 0a5.002 5.002 0 019.288 0M15 7a3 3 0 11-6 0 3 3 0 016 0zm6 3a2 2 0 11-4 0 2 2 0 014 0zM7 10a2 2 0 11-4 0 2 2 0 014 0z" /></svg>`
      },
      {
        to: '/landlord/contracts',
        label: 'Hợp đồng',
        icon: `<svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke="currentColor"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M9 12h6m-6 4h6m2 5H7a2 2 0 01-2-2V5a2 2 0 012-2h5.586a1 1 0 01.707.293l5.414 5.414a1 1 0 01.293.707V19a2 2 0 01-2 2z" /></svg>`
      },
      {
        to: '/landlord/invoices',
        label: 'Hóa đơn',
        icon: `<svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke="currentColor"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M9 7h6m0 10v-3m-3 3h.01M9 17h.01M9 14h.01M12 14h.01M15 11h.01M12 11h.01M9 11h.01M7 21h10a2 2 0 002-2V5a2 2 0 00-2-2H7a2 2 0 00-2 2v14a2 2 0 002 2z" /></svg>`
      }
    ];

    const tenantGeneral = [
      {
        to: '/tenant',
        label: 'Tổng quan & Hóa đơn',
        icon: `<svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke="currentColor"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M3 12l2-2m0 0l7-7 7 7M5 10v10a1 1 0 001 1h3m10-11l2 2m-2-2v10a1 1 0 01-1 1h-3m-6 0a1 1 0 001-1v-4a1 1 0 011-1h2a1 1 0 011 1v4a1 1 0 001 1m-6 0h6" /></svg>`
      }
    ];

    const handleLogout = () => {
      authStore.logout();
      router.push('/login');
    };

    return {
      username,
      role,
      isSidebarOpen,
      closeSidebar,
      userInitial,
      roleLabel,
      landlordGeneral,
      landlordManage,
      tenantGeneral,
      handleLogout,
    };
  },
};
